public class KunciVar {
    public static void main(String[] args) {
        //var harus langsung diinisiasi, var berlaku untuk semua tipe
        final String name = "Samuel I. H. Sibuea";
        var age = 20;
        var alamat = "Laguboti";


        System.out.println("Nama: "+name);
        System.out.println("Usia: "+age);
        System.out.println("Alamat: "+ alamat);
    }
}
