public class TipeDataNumber {
    public static void main(String[] args) {

        // Tipe data Integer Number
        byte iniByte = 100;
        short iniShort = 1000;
        int iniInt =100000000;
        long iniLong = 100000000L;

        // Tipe data Floating Point Number
        float iniFloat = 10.12F;
        double iniDouble = 10.10d;

        // Tipe data Literals
        int decimalInt = 25;
        int hexDecimal = 0xA132B;
        int binDecimal = 0b100101;

        // Underscore agar mempermudah pembacaan angka besar
        long balance = 1_000_000_000_000L;
        int sum = 60_000_000;

        System.out.println(iniByte);
        System.out.println(iniShort);
        System.out.println(iniInt);
        System.out.println(iniLong);
        System.out.println(iniFloat);
        System.out.println(iniDouble);


    }
}
